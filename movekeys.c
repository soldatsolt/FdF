#include "fdf.h"

void	uprld(int key, t_fdf *fdf)
{
	if (key == 126)
		(*fdf).d3d.ox -= 0.05;
	if (key == 124)
		(*fdf).d3d.oz -= 0.05;
	if (key == 125)
		(*fdf).d3d.ox += 0.05;
	if (key == 123)
		(*fdf).d3d.oz += 0.05;
}
