#include "fdf.h"

void	uprld(int key, t_fdf *fdf)
{
	if (key == 126)
		(*fdf).img.y -= 4;
	if (key == 124)
		(*fdf).img.x += 4;
	if (key == 125)
		(*fdf).img.y += 4;
	if (key == 123)
		(*fdf).img.x -= 4;
}
